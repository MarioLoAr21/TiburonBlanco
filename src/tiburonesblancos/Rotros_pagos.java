    private void bRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bRegistrarActionPerformed
        // TODO astemdd your handling code here:valil
        cont=0;
        validar();
        if(cont==0)
        {
            String ps3;
            if(!"".equals(d1.getText())){ ps3=d1.getText();}else{ps3=null;}
            String ps4;
            if(!"".equals(datNombre.getText())){ ps4=datNombre.getText();}else{ps4=null;}
            String ps5;
            if(!"".equals(datApellidoP.getText())){ ps5=datApellidoP.getText();}else{ps5=null;}
            String ps7;
            if(!"".equals(d11.getText())){ ps7=d11.getText();}else{ps7=null;}
            Connection con=null;
            try{
                con=getConection();

                ps= con.prepareStatement("INSERT INTO otros_pagos (id_otros_pagos,clasificacion,id_alumno,nombre,apellidoP,cantidad,folio,fecha) VALUES(?,?,?,?,?,?,?,?)");
                ps.setString(1,null);
                ps.setString(2,jComboBox1.getSelectedItem().toString());
                ps.setString(3,ps3);
                ps.setString(4,ps4);
                ps.setString(5,ps5);
                ps.setString(6,d6.getText());
                ps.setString(7,ps7);
                ps.setString(8,d9.getText()+"-"+d8.getText()+"-"+d7.getText());
                
                int res=ps.executeUpdate();
                if(res>0)
                {
                    JOptionPane.showMessageDialog(rootPane,"Pago Registrado");
                    limpiarCajas();
                }
                else
                {
                    JOptionPane.showMessageDialog(rootPane,"Error al Registrar");
                    limpiarCajas();
                }
                con.close();
            }
            catch(Exception e)
            {
                System.out.println(e);
            }
        }
        else
        {
            JOptionPane.showMessageDialog(rootPane,"Rellene los campos requeridos");
        }
    }//GEN-LAST:event_bRegistrarActionPerformed

